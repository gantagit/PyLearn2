# = is assignation operator
# == is comparison operator, And it returns always a boolean value
# >= is greater than or equal to
# <= is less than or equal to
# != is not equal to

# comparison operators
a = int(input("Integer value for a = "))
b = int(input("Integer value for b = "))
print("a = {}, b = {}".format(a, b))

if a >= b:
    print(True)
else:
    print(False)

# unary operators
x = 36
y = -1
z = x + y # BODMAS principle in math
print("value of x = {}, y = {},  z = {}".format(x, y, z))

# NOT operator
is_active_student = True
print("is_active_student = {}".format(is_active_student))
print("NOT of is_active_student = {}".format(not is_active_student))

