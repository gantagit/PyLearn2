# Examples of complex Data types

# List Example
a = [2, 'abc', 3]
print(a)
print(type(a))

# tuple example
tuple_a = (2, 'abc', 3)
print(tuple_a)
print(type(tuple_a))

# append list
a.append('vijay')
print(a)

# dictionary example
dict_a = {'name': 'vijay', 'age': 30}
print(dict_a)
print(type(dict_a))

# set example
set_a = {2, 'abc', 3, 3.14}
print(set_a)
print(type(set_a))
