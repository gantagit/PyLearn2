# Loops
# Repeat a block of code multiple times
# +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# the above program with lower and higher limits for range
x = int(input("Enter start of the index : \n"))
y = int(input("Enter range : \n"))

for i in range(x, y):
    print("hello_world ", i)
