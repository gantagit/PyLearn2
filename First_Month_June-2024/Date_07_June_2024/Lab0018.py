# List

shopping_list = ["milk", "eggs", "bread", "apples"]
print(len(shopping_list))
print(shopping_list)
print(type(shopping_list))

shopping_list[0] = "Vijay"
print(len(shopping_list))
print(shopping_list)
print(type(shopping_list))

shopping_list.append("Banana")
print(len(shopping_list))
print(shopping_list)
print(type(shopping_list))
