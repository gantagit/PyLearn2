# Type conversions
def convert_to_int(string):
    return int(string)


def convert_to_float(string):
    return float(string)  # float() converts a string or a number to a floating-point number


# Example usage
num_str = "3.14"
num_float = convert_to_float(num_str)
print(num_float)  # Output: 3.14
